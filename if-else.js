const semaforo = "amarelo";

if (semaforo === "verde") {
    console.log("Pode passar");
} else if (semaforo === "amarelo") {
    console.log("Atenção");
} else if (semaforo === "vermelho") {
    console.log("Pare");
}
// O código acima verifica o valor da variável "semaforo" e imprime uma mensagem correspondente com base no seu valor. Se o semáforo for verde, ele indica que é seguro passar. Se for amarelo, ele alerta para ter atenção. Se for vermelho, ele instrui a parar.