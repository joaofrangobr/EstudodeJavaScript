let time = ["Manhã","tarde","noite"]; // aqui temos o exemplo de uma array, no caso uma lista de coisas que queremos

console.table(time); // o console.table ele puxa os itens pedidos em forma de tabela com o inicio em 0

console.log(time[0]); // colocando entre [] e o número desejado do item que queremos ele aparecera por escrito

time[1] = "por do sol"; // aqui podemos mudar ou mandar um item da lista tornando ele em outro

console.table(time); // após a mudança o item aparecerá atualizado com a nova mudança feita aqui