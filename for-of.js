const cars = ['BMW', 'Volvo', 'Mini'];

for (let car of cars) {
    console.log(car); // O código acima é um exemplo de um loop "for...of" em JavaScript. Ele itera sobre os elementos do array "cars", e a cada iteração, a variável "car" recebe o valor do elemento atual do array. Dentro do bloco de código do loop, o valor de "car" é impresso no console. Portanto, o loop imprimirá os nomes dos carros ("BMW", "Volvo", "Mini") no console, um por linha.
}