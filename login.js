const login = "joao"; // O código acima define uma variável chamada "login" e atribui a ela o valor "joao1". Essa variável pode ser usada posteriormente para verificar se o login do usuário é correto ou não.
const password = "123456";

if (login === "joao" && password === "123456") {
    console.log("Usuário conectado")
} else {
    console.log("login ou senha incorretos")
} // O código acima verifica se o valor da variável "login" é igual a "joao" e se o valor da variável "password" é igual a "123456". Se ambas as condições forem verdadeiras, ele imprime "Usuário conectado". Caso contrário, ele imprime "login ou senha incorretos".